<h1 class="heading">What is TraceMe?</h1>
<p class="paragraph">TRACEME is an educational web app that allows children to learn varies topics such as how to write the alphabet, numbers, and how to draw basic shapes. The parents would set up the environment with just a click of a button, and children will be able to use the webcam and the gloveme (a glove with LED's located on the tip of the index finger) to start drawing letters or numbers out of thin air. The webcam tracks the LED light on the tip of the index finger and the child will attempt to trace the dashed outlines that appears on the webcam feedback. Red LED is for writing, and the green LED is for erasing. There are multiple lessons that the users can choose from.</p>
    
